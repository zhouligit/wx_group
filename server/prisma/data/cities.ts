/** 展示用短名：广州市 → 广州，内蒙古自治区 → 内蒙古 */
export function regionShortName(name: string): string {
  return name
    .replace('维吾尔自治区', '')
    .replace('壮族自治区', '')
    .replace('回族自治区', '')
    .replace('特别行政区', '')
    .replace('自治区', '')
    .replace('省', '')
    .replace('市', '');
}

